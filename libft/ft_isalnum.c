#include "libft.h"
#include <stdio.h>

int	ft_isalnum(int a)
{
	return (ft_isalpha(a) || ft_isdigit(a));
	return ((a >= 'a' && a <= 'z')
		|| (a >= 'A' && a <= 'Z')
		|| (a >= '0' && a <= '9'));
}
