#include "libft.h"

int	ft_atoi(const char *str)
{
	int	m;
	int	i;
	int	n;

	i = 0;
	m = 1;
	n = 0;
	while ((*(str + i) >= 9 && *(str + i) <= 13) || *(str + i) == ' ')
		i++;
	m = (*(str + i) == '-') * -2 + 1;
	if (*(str + i) == '+' || *(str + i) == '-')
		i++;
	while (1)
	{
		if (*(str + i) < '0' || *(str + i) > '9')
			return (m * n);
		n = n * 10 + *(str + i) - '0';
		i++;
	}
	return (0);
}
